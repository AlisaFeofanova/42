#include "../includes/push_swap.h"

static int	get_max_bits(t_stack *stack)
{
	int	max_idx;
	int	max_bits;

	max_idx = 0;
	max_bits = 0;
	while (stack)
	{
		if (stack->index > max_idx)
			max_idx = stack->index;
		stack = stack->next;
	}
	while ((max_idx >> max_bits) != 0)
		max_bits++;
	return (max_bits);
}

void	radix_sort(t_stack **stack_a, t_stack **stack_b)
{
	int	size;
	int	max_bits;
	int	i;
	int	j;

	size = stack_size(*stack_a);
	max_bits = get_max_bits(*stack_a);
	i = 0;
	while (i < max_bits)
	{
		j = 0;
		while (j < size)
		{
			if ((((*stack_a)->index >> i) & 1) == 1)
				ra(stack_a);
			else
				pb(stack_a, stack_b);
			j++;
		}
		while (*stack_b)
			pa(stack_a, stack_b);
		i++;
	}
}
